class Product {
  final String id;
  final String name;
  final String image;
  final String seller;
  final double price;
  final String description;
  final DateTime datePosted;

  Product({
    required this.id,
    required this.name,
    required this.image,
    required this.seller,
    required this.price,
    required this.description,
    required this.datePosted,
  });
}
